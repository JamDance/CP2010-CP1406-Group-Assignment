            <h3>Simple Easy Tips When Starting to Play the Piano</h3>
            <h4 class="paragraphHeader">Just Relax</h4>
            <p>
                If your fingers are rigid, stiff, with the anxiety of trying to get the right notes at the right time, it will be much more difficult to get it right. Touching the piano keys lightly and gently will make it  much easier to get the the right place at the right time. A relaxed hand is much easier to get the the right place at the right time.
            </p>

            <h4 class="paragraphHeader">Enjoy Yourself</h4>
            <p>Play something you like and that you are familiar with. In time, you will be able to look at any sheet of music and play something close to what is written, but there is no sense adding the burden of playing unfamiliar music on top of learning to press the right key. When we learned to type, we learned by typing familiar words. We didn't learn to type obscure chemical names or technical jargon. The same applies to the piano. When starting out, play what is fun for you. Anything you play will develop your skill.

                If you are practicing to play by ear, play what you are familiar with: advertising jingles, TV theme music, popular folk songs, etc.
            </p>

            <h4 class="paragraphHeader">For improving Fluidity</h4>
            <p>
                Try to play by ear and your fluidity may increase greatly.  In other words play without any music, sit down and play the melody to some songs that are very familiar to you. This should be music that you have not previously played from a printed page.</p>

            <p>You don't have to become an expert at playing by ear, but if you practice until you can do it a little bit, it will greatly improve your sight reading capabilities!</p>

            <h4 class="paragraphHeader">Keeping the Rhythm</h4>
            <p>
                The trick is to play what you play in the proper rhythm, even if you only play a portion of the song. In other  words, practice keeping the rhythm going at the expense of everything else. Don't play the cords, or play one or two notes in the cord rather than the whole thing.  Before long, you will be able to get more and more notes included in the cords, or you will learn which notes are optional. In most cases nobody but you will realize that there is a note missing.</p>
